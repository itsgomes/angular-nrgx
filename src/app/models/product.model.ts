export interface IProduct {
  id: number;
  title: string;
  description: string;
  price: number;
  discountPercentage: number;
  rating: number;
  stock: number;
  brand: string;
  category: string;
  thumbnail: string;
  images: string[];
}

export function sortProductByPrice(product1: IProduct, product2: IProduct) {
  return product1.price - product2.price;
}